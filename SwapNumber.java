public class SwapNumber {
    public static void main(String[] args) {
        // Initialize two variables
        int num1 = 12;
        int num2 = 53;

        // Swap Algo
        int temp = num1;
        num1 = num2;
        num2 = temp;

        System.out.println("Now value of num1 is : " + num1);
        System.out.println("Now value of num2 is : " + num2);

    }
}
